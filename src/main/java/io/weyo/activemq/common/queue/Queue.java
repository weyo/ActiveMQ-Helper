package io.weyo.activemq.common.queue;

/**
 * Queue
 * @author weyo
 */
public class Queue {
	private String name;
	private long number;
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public long getNumber() {
		return number;
	}
	
	public void setNumber(long number) {
		this.number = number;
	}
}
